public class CapitalRecordType {
    public String deposit;//存款
    public String recommend;//推荐
    public String favorable;//优惠
    public String transfers;//转账
    public String backwater;//反水
    public String withdrawals;//取款

    public String getDeposit() {
        return deposit;
    }

    public void setDeposit(String deposit) {
        this.deposit = deposit;
    }

    public String getRecommend() {
        return recommend;
    }

    public void setRecommend(String recommend) {
        this.recommend = recommend;
    }

    public String getFavorable() {
        return favorable;
    }

    public void setFavorable(String favorable) {
        this.favorable = favorable;
    }

    public String getTransfers() {
        return transfers;
    }

    public void setTransfers(String transfers) {
        this.transfers = transfers;
    }

    public String getBackwater() {
        return backwater;
    }

    public void setBackwater(String backwater) {
        this.backwater = backwater;
    }

    public String getWithdrawals() {
        return withdrawals;
    }

    public void setWithdrawals(String withdrawals) {
        this.withdrawals = withdrawals;
    }
}
